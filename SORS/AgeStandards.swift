                    terpolate = (dist - 25.0) / 5.0
                }
                if std.distance == 30 {
                    high = std.standard
                }
            } else if dist <= 40 {
                if std.distance == 30 {
                    low = std.standard
                    terpolate = (dist - 30.0) / 10.0
                }
                if std.distance == 40 {
                    high = std.standard
                }
            } else { // dist > 40
                if std.distance == 40 {
                    return Double(std.standard) + ((dist/40.0 - 1.0) * Double(std.standard))
                }
            }
        }
    }
    // interpolate low and high
    return (Double(low) + Double(high - low) * terpolate)
}
